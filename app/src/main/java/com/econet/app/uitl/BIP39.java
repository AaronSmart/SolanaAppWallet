    public static List getRandomWallet()
    {
        ArrayList<String> walletList=new ArrayList<>();
        Set<Integer> set= new HashSet<>();
        int count=0;
        while(true)
        {
            int temp =getRandomNumber();
            if(!set.contains(temp))
            {
                set.add(temp);
                walletList.add((String) getBIPMap().get(temp));
                count++;
                if(count==24)
                {
                    break;
                }
            }
        }
        return walletList;
    }
    public static String getmnemonicString(ArrayList list)
    {
        String result="";
        for(int i=0;i<list.size();i++)
        {
            result= result+list.get(i)+" ";
        }
        return  result.trim();
    }


