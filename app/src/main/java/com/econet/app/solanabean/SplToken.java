package com.econet.app.solanabean;

public class SplToken {
    public String tokenName;
    public String tokenAmount;
    public String tokenMint;

    public String getTokenName() {
        return tokenName;
    }

    public void setTokenName(String tokenName) {
        this.tokenName = tokenName;
    }

    public String getTokenAmount() {
        return tokenAmount;
    }

    public void setTokenAmount(String tokenAmount) {
        this.tokenAmount = tokenAmount;
    }

    public String getTokenMint() {
        return tokenMint;
    }

    public void setTokenMint(String tokenMint) {
        this.tokenMint = tokenMint;
    }
}
